public class Main {
    public static void main(String[] args) {
      //  System.out.println("Hello world!");
        // Pyramid of the Characters

        char i,j;
        for(i='A';i<='D';i++){
            for(j='A';j<=i;j++){
                System.out.print(j+" ");
            }
            System.out.println("\n");
        }
    }
}